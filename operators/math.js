// To multiplicate
console.log(5*5)

// To devide
console.log(10/2)

// To sum and substract
console.log(5+5)
console.log(10-1)

// Remainder - the rest of a division
console.log(11%9)

// Increment
let increment = 0
increment++
console.log(increment)

// Decrement
let decrement = 0
decrement--
console.log(decrement)

// Exponetial
console.log(10**2)

